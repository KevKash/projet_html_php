Comme pour le plan, on trouve trois types de paragraphes dans la dissertation critique (on peut obtenir des paragraphes hybrides qui mêlent ces trois types) :

Le paragraphe démonstratif présentera d'abord l'idée énoncée, ensuite les parties illustratives (idée illustrée) et les parties explicatives (idée expliquée), puis enfin un rappel de l'idée énoncée avec une transition.

Le paragraphe dialectique avance d'abord une idée défendant un point de vue (thèse) avec illustration et explication, pour ensuite l'opposer à l'idée contraire (antithèse) avec illustration et explication pour finalement conclure et donner la véritable idée énoncée du paragraphe (synthèse). Ce type de paragraphe est plus complexe et exige une plus grande habileté d'écriture, à défaut de laquelle il peut facilement donner l'impression que l'élève se contredit.

Le paragraphe comparatif fonctionne comme le paragraphe démonstratif, sauf qu'à l'intérieur du même paragraphe, on établit la démonstration entre deux éléments que l'on veut comparer. L'exemple qui suit montre ce type de paragraphe.</p>
